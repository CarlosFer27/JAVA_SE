package Video_SE7;

public class Sec_escape {
	
	public static void main(String[] args){
		
		System.out.println("Te amo \n mi Fernandita");
		System.out.println("Te amo \"Fernanduchis\"");
		System.out.println("Te amo \t \t Cachetitos");
		System.out.println("Te amo Marifer\\\\\\");
		/*aqui vemos distintos secuencias de escape por ejemplo:
		 * \n que es un salto de linea
		 * \" que nos sirve para imprimir con doble comilla
		 * \t que nos sirve para imprimir una tabulacion
		 * \\ nos permite imprimir la barra invertida
		 * 
		 * Podemos observar que "\" es nuestro caracter de escape el cual se comportara de forma distina
		 * dependiendo el caracter que le siga del lado derecho
		 */
	
	
	}

}
